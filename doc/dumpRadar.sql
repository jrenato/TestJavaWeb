-- Table: "COORDENADA"

-- DROP TABLE "COORDENADA";

CREATE TABLE "COORDENADA"
(
  "ID" integer NOT NULL,
  "LATITUDE" double precision,
  "LONGITUDE" double precision,
  CONSTRAINT "PK_COORDENADA_ID" PRIMARY KEY ("ID")
)
WITH (
  OIDS=FALSE
);

-- Table: "ENDERECO"

-- DROP TABLE "ENDERECO";

CREATE TABLE "ENDERECO"
(
  "ID" integer NOT NULL,
  "LOGRADOURO" character varying(100),
  "NUMERO" integer,
  "CIDADE" character varying(100),
  "ESTADO" character varying(2),
  CONSTRAINT "PK_ENDERECO" PRIMARY KEY ("ID")
)
WITH (
  OIDS=FALSE
);
ALTER TABLE "ENDERECO" OWNER TO postgres;

-- Table: "TELEFONE"

-- DROP TABLE "TELEFONE";

CREATE TABLE "TELEFONE"
(
  "ID" integer NOT NULL,
  "TELEFONE" character varying(20),
  "TIPO" character varying(50),
  "ENDERECO_ID" integer,
  CONSTRAINT "PK_TELEFONE_ID" PRIMARY KEY ("ID")
)
WITH (
  OIDS=FALSE
);
ALTER TABLE "TELEFONE" OWNER TO postgres;

-- Table: "ESTABELECIMENTO"

-- DROP TABLE "ESTABELECIMENTO";

CREATE TABLE "ESTABELECIMENTO"
(
  "ID" integer NOT NULL,
  "NOME" character varying(100),
  "FK_COORDENADA_ID" integer,
  "FK_ENDERECO_ID" integer,
  CONSTRAINT "PK_ESTABELECIMENTO_ID" PRIMARY KEY ("ID"),
  CONSTRAINT "FK_COORDENADA" FOREIGN KEY ("FK_COORDENADA_ID")
      REFERENCES "COORDENADA" ("ID") MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT "FK_ENDERECO" FOREIGN KEY ("FK_ENDERECO_ID")
      REFERENCES "ENDERECO" ("ID") MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
)
WITH (
  OIDS=FALSE
);
ALTER TABLE "ESTABELECIMENTO" OWNER TO postgres;

-- Index: "FKI_COORDENADA"

-- DROP INDEX "FKI_COORDENADA";

CREATE INDEX "FKI_COORDENADA"
  ON "ESTABELECIMENTO"
  USING btree
  ("FK_COORDENADA_ID");

-- Index: "FKI_ENDERECO"

-- DROP INDEX "FKI_ENDERECO";

CREATE INDEX "FKI_ENDERECO"
  ON "ESTABELECIMENTO"
  USING btree
  ("FK_ENDERECO_ID");
